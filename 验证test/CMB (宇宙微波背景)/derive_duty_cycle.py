"""
derive_duty_cycle.py
从理论本征参数 (λ, Vc) 导出宇宙平均暗物质密度 omch2
通过占空比因子将星系内部蜷缩态密度映射到宇宙平均密度
"""

import numpy as np

# ============================================================
# 1. 理论本征参数（已锁定，不做任何拟合）
# ============================================================
lam = 3.27e43          # λ (M⊙/kpc³)，由恒星质量下限 0.35M⊙ 锁定
Vc  = 1.00e6           # Vc (M⊙/kpc³)，蜷缩态固定能量密度，169星系锁定

# ============================================================
# 2. 宇宙学基本观测常数
# ============================================================
# 宇宙临界密度 ρ_crit₀ = 3H₀²/(8πG)
H0_obs      = 69.0     # km/s/Mpc，晚期哈勃常数（由 m² 导出）
H0_si       = H0_obs * 1000 / (3.086e22)  # 转换为 1/s
G_si        = 6.674e-11  # m³/kg/s²
rho_crit_si = 3 * H0_si**2 / (8 * np.pi * G_si)  # kg/m³
Msun_kg     = 1.989e30  # kg
kpc_m       = 3.086e19  # m
rho_crit_Msun_kpc3 = rho_crit_si * (kpc_m**3) / Msun_kg  # M⊙/kpc³

print("=" * 65)
print("  从 Vc 到宇宙平均暗物质密度的推导")
print("=" * 65)
print(f"\n  宇宙临界密度 ρ_crit₀ = {rho_crit_Msun_kpc3:.2e} M⊙/kpc³")
print(f"  蜷缩态能级 Vc       = {Vc:.2e} M⊙/kpc³")
print(f"  比值 Vc/ρ_crit₀      = {Vc/rho_crit_Msun_kpc3:.2e}")
print(f"\n  如果全宇宙都是蜷缩态，Ω_c ≈ {Vc/rho_crit_Msun_kpc3:.2e}")
print(f"  这意味着宇宙早已坍缩 → 蜷缩态只存在于星系内部。")

# ============================================================
# 3. 占空比因子的估计
# ============================================================
# 占空比因子 = 星系在宇宙体积中的占比
# 通过星系数密度和典型星系暗物质晕体积来估算

# 观测约束：星系暗物质晕的总质量范围
M_halo_min = 1e9    # M⊙，矮星系暗物质晕最小质量
M_halo_max = 1e15   # M⊙，巨椭圆星系暗物质晕最大质量

# 宇宙平均暗物质密度（Planck 2018 实测）
Omega_c_h2 = 0.120   # 由Planck实测
h = 0.69
Omega_c = Omega_c_h2 / h**2  # ≈ 0.252

# 宇宙平均暗物质密度（M⊙/kpc³）
rho_DM_cosmic = Omega_c * rho_crit_Msun_kpc3

print(f"\n  Planck实测 Ω_c = {Omega_c:.4f}")
print(f"  宇宙平均暗物质密度 = {rho_DM_cosmic:.2e} M⊙/kpc³")

# ============================================================
# 4. 占空比因子的计算
# ============================================================
# 占空比因子 = 宇宙平均密度 / 星系内部密度
duty_cycle = rho_DM_cosmic / Vc

print(f"\n  占空比因子 duty_cycle = {duty_cycle:.2e}")
print(f"  物理含义：星系内部蜷缩态体积占宇宙总体积的 {duty_cycle*100:.4f}%")

# ============================================================
# 5. 占空比因子的独立验证（通过星系数密度）
# ============================================================
# 方法一：通过星系暗晕质量函数
# 宇宙中星系数密度 ≈ 0.01-0.1 / Mpc³（暗晕质量 > 10^11 M⊙）
n_halo = 0.03          # /Mpc³，典型值
Mpc3_to_kpc3 = 1e9     # 1 Mpc³ = 10^9 kpc³
n_halo_kpc3 = n_halo / Mpc3_to_kpc3  # /kpc³

# 典型暗晕体积（取暗晕特征半径 ~100 kpc，体积 ~4.2×10^6 kpc³）
r_halo_typical = 100.0   # kpc
V_halo = (4/3) * np.pi * r_halo_typical**3

# 暗晕总体积占比
f_halo = n_halo_kpc3 * V_halo

print(f"\n  === 独立验证（通过星系数密度）===")
print(f"  典型星系数密度 = {n_halo:.2f} /Mpc³")
print(f"  典型暗晕半径     = {r_halo_typical:.0f} kpc")
print(f"  暗晕总体积占比   = {f_halo:.2e}")
print(f"  与方法一（占空比因子）比值 = {f_halo/duty_cycle:.2f}")

# ============================================================
# 6. 理论导出 omch2
# ============================================================
# 理论导出值：omch2_theory = (Vc * duty_cycle) / ρ_crit₀ × h²
omch2_theory = (Vc * duty_cycle) / rho_crit_Msun_kpc3 * h**2

print(f"\n  === 理论导出值 === ")
print(f"  理论 omch2 = {omch2_theory:.4f}  (占空比方法)")
print(f"  Planck实测 = {Omega_c_h2:.4f}")
print(f"  偏差 = {abs(omch2_theory-Omega_c_h2)/Omega_c_h2*100:.1f}%")

# ============================================================
# 7. 占空比因子的物理来源
# ============================================================
print(f"\n  === 占空比因子的物理来源 === ")
print(f"  占空比因子由凝聚占比 f_cond 决定：")
print(f"    - 凝聚区域 → 生成粒子 → 星系形成处")
print(f"    - 未凝聚区域 → 背景蜷缩场 → 星系际空间")
print(f"  f_cond 由凝聚临界条件 ρ_cond(λ) 决定：")
print(f"    λ = {lam:.2e} M⊙/kpc³")
print(f"    φ₀ = {(4*Vc/lam)**0.25:.2e}")
print(f"    ρ_cond ~ λ φ₀⁴ ≈ {lam * (4*Vc/lam):.2e} M⊙/kpc³")
print(f"  凝聚占比 f_cond = 宇宙中满足 ρ ≥ ρ_cond 的体积份额")
print(f"  后续需通过凝聚动力学数值模拟确定 f_cond 的精确值。")
print("=" * 65)