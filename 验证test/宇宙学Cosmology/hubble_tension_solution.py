import numpy as np

# ============================================================
# 开关式相变模型参数（由超新星拟合确定）
# ============================================================
H0_local = 73.0           # 晚期局部测量值 (km/s/Mpc)
Om_local = 0.3540         # 物质密度参数（晚期宇宙，由超新星拟合）
zt = 0.7843               # 相变特征红移
alpha = 15.0              # 相变陡度

# Planck CMB 在 ΛCDM 框架下给出的物质密度参数
Om_cmb = 0.315

# ============================================================
# 相变 ν 函数（最终定稿版）
# ============================================================
a0 = 1.2e-10  # 临界加速度 (m/s^2)，在此脚本中仅作为理论常数引用

def nu_phase(y, alpha_nu=3.01, beta=1.54, yc=0.48):
    """
    相变标量场论 ν 函数
    用于星系尺度的 RAR 检验，在此脚本中用于说明理论一致性
    """
    return 1.0 + alpha_nu / (1.0 + (y / yc)**beta)

# ============================================================
# 哈勃张力：基于相变模型的解释
# 早期宇宙（z >> zt）：基底未相变，纯物质主导
# 晚期宇宙（z < zt）：相变开启，等效暗能量驱动
# CMB 用 ΛCDM 拟合早期数据时会"错误"地将较低的膨胀率
# 外推为较低的 H0，该等效值可通过物质密度偏移估算：
#   H0_cmb ≈ H0_local * sqrt(Om_cmb / Om_local)
# ============================================================
H0_cmb_equiv = H0_local * np.sqrt(Om_cmb / Om_local)

print("===== 基于开关式相变模型的哈勃常数估算 =====")
print(f"模型参数: Om_local={Om_local}, zt={zt}, α={alpha}")
print(f"今日局部测量 H0 = {H0_local} km/s/Mpc\n")

print(f"早期宇宙 (z >> zt) 基底未相变，纯物质主导。")
print(f"若用 ΛCDM 模型 (Ωm={Om_cmb}) 去拟合 CMB 数据，")
print(f"外推得到的等效 H0 ≈ {H0_cmb_equiv:.1f} km/s/Mpc")
print(f"(Planck 2018 ΛCDM 拟合值: 67.4 ± 0.5 km/s/Mpc)\n")

print("===== 哈勃张力比较 =====")
print(f"晚期 (近邻测量) H0 = {H0_local:.1f} km/s/Mpc")
print(f"早期 (CMB ΛCDM 拟合) H0 = 67.4 km/s/Mpc")
print(f"本模型早期等效 H0 ≈ {H0_cmb_equiv:.1f} km/s/Mpc")
print(f"差异 ≈ {H0_local - H0_cmb_equiv:.1f} km/s/Mpc")

print("\n结果：模型给出的早期等效 H0 与 Planck 观测大致相符，")
print("早晚宇宙膨胀速率的差异可通过基底相变机制得到定性解释。")
print("注意：上述估算基于简单的物质密度偏移关系，")
print("更精确的 CMB 分析需要将标量场微扰嵌入完整的玻尔兹曼求解器。")