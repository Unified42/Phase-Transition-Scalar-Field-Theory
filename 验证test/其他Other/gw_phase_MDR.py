import h5py
import numpy as np

# ============================================================
# 理论常数
# ============================================================
a0 = 1.2e-10          # 相变临界加速度 (m/s²)
c = 2.99792458e8      # 光速 (m/s)

# ============================================================
# 读取 GW170817 光度距离后验样本
# 数据来源：LIGO/Virgo GWTC-1 参数估计公开数据
# 使用低自旋波形模型 IMRPhenomPv2NRT 的后验样本
# ============================================================
file = r"data/GW170817_GWTC-1.hdf5"
with h5py.File(file, 'r') as f:
    dset = f['IMRPhenomPv2NRT_lowSpin_posterior']
    distance_Mpc = dset['luminosity_distance_Mpc'][:]
distance_m = distance_Mpc * 3.085677581e22   # Mpc 转为米

median_dist = np.median(distance_m)
print(f"GW170817 光度距离中位数: {np.median(distance_Mpc):.1f} Mpc")

# ============================================================
# 理论色散延迟量级估算
# 基本假设：引力波速度修正与频率相关，Δv/c ∝ (f/f*)^n
# 其中 f* = a0/c 是基底相变的特征频率（量纲分析）
# 耦合常数 κ 取保守估计：κ ≈ f*/f_gw（对应 n=1 情形）
# 这假设了色散修正与频率的线性关系
# 时间延迟：Δt ≈ (D_L / c) × (Δv/c) ≈ (D_L / c) × κ
# ============================================================
fstar = a0 / c                       # 理论特征频率，约 4e-19 Hz
f_gw = 100.0                         # GW170817 典型引力波频率
kappa_est = fstar / f_gw             # 耦合常数估计，约 4e-21
delta_t_max = median_dist / c * kappa_est   # 理论色散时延

print(f"\n理论特征频率 f* = {fstar:.2e} Hz")
print(f"对于 {f_gw} Hz 引力波，耦合常数 κ ≈ f*/f_gw = {kappa_est:.2e}")
print(f"理论预计色散时延量级：Δt ≈ {delta_t_max:.2e} s")

# ============================================================
# LVK 对 GW170817 的 MDR 约束（参考已发表结果）
# LVK 对修正色散关系参数 A_α 给出了 90% 置信上限
# 以下取 α=0 时的最宽松约束作为典型参考值
# 实际约束随色散指数 α 的不同而变化
# 参考：LIGO/Virgo/KAGRA GWTC-3 通用相对论检验
# ============================================================
# LVK 对 α=0 的 90% 置信上限约为 A_0 < 1e-15 eV^{-1}
# 该值对应的色散时延量级约 1e-6 s（粗略转换，非精确值）
A_alpha_upper = 1e-15       # 90% 置信上限，单位 eV^{-1}
delta_t_LVK = A_alpha_upper * 1e9   # 粗略转换为时间 (s)

print(f"\nLVK 对 α=0 的 90% 置信上限：A ≲ {A_alpha_upper:.0e} eV^{-1}")
print(f"对应色散时延量级约为 {delta_t_LVK:.2e} s")
print(f"理论预言的色散时延（约 {delta_t_max:.2e} s）远小于当前约束上限。")

print("\n--- 结论 ---")
print("本理论预言的引力波色散时延量级远小于当前 LIGO/Virgo 探测灵敏度。")
print("现有引力波数据无法排除该理论，也未提供对其的正面支持。")
print("未来更高灵敏度的引力波探测器可能逐步提升色散检验的能力。")